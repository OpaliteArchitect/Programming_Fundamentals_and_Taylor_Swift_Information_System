        public static void Play(int cycles = 1, bool playHalf = false)
        {
            for (int i = 0; i < cycles; i++)
            {
                PlayOneCycle(playHalf);
            }
        }

        private static void PlayOneCycle(bool playHalf)
        {
            if (!playHalf)
            {
                foreach (string frame in DanceFrames)
                {
                    Console.Clear();
                    PrintCenteredFrame(frame);
                    Thread.Sleep(200);
                }
            }
            else
            {
                for (int i = 1; i <= 4; i++)
                {
                    Console.Clear();
                    PrintCenteredFrame(DanceFrames[i]);
                    Thread.Sleep(200);
                }
            }
        }
        private static void PrintCenteredFrame(string frame)
        {
            // 1. Split the frame into individual lines
            // Use StringSplitOptions.RemoveEmptyEntries to handle blank lines and common line endings
            string[] lines = frame.Split(['\r', '\n'], StringSplitOptions.RemoveEmptyEntries);

            int windowWidth = Console.WindowWidth;
            int windowHeight = Console.WindowHeight;
            int frameHeight = lines.Length;

            // 2. Calculate vertical padding (blank lines above the frame)
            // Integer division is used to find the number of blank lines needed above the frame.
            int verticalPadding = (windowHeight - frameHeight) / 2;

            // 3. Guardrail: Prevent negative padding (if frame is taller than console)
            if (verticalPadding < 0) verticalPadding = 0;

            // 4. Apply top vertical padding (to center vertically)
            for (int i = 0; i < verticalPadding; i++)
            {
                Console.WriteLine();
            }

            // 5. Apply horizontal centering for each line
            foreach (string line in lines)
            {
                // Calculate the necessary left padding to center the line
                int horizontalPadding = (windowWidth - line.Length) / 2;

                // Guardrail: Prevent negative padding if the line is wider than the console window
                if (horizontalPadding < 0) horizontalPadding = 0;

                // Create the padding string and write the centered line
                string centeredLine = new string(' ', horizontalPadding) + line;
                Console.WriteLine(centeredLine);
            }
        }

